using System;

namespace _4_Type_conversion_tutorial
{
    class Program
    {
        static void Main(string[] args)
        {
            double d = 5768.23;
            int i;
            i = (int)d;
            Console.WriteLine(i);
            int n = 10;
            float f = 10.3f;
            double o = 1987.25;
            bool b = true;
            Console.WriteLine(n.ToString());
            Console.WriteLine(f.ToString());
            Console.WriteLine(o.ToString());
            Console.WriteLine(b.ToString());


        }
    }
}
